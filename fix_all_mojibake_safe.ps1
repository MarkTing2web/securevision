$targetFiles = Get-ChildItem -Path . -Filter "*.html" -Recurse

foreach ($file in $targetFiles) {
    Write-Host "Processing $($file.FullName)..."
    $bytes = [System.IO.File]::ReadAllBytes($file.FullName)
    $content = [System.Text.Encoding]::UTF8.GetString($bytes)
    $modified = $false
    
    foreach ($rep in $replacements) {
        $mangled = [System.Text.Encoding]::UTF8.GetString($rep.t)
        $correct = [System.Text.Encoding]::UTF8.GetString($rep.r)
        
        if ($content.Contains($mangled)) {
            $content = $content.Replace($mangled, $correct)
            $modified = $true
        }
    }
    
    # Generic fixes for common mangled sequences
    # â€“ (En dash)
    if ($content.Contains([char]0xE2 + [char]0x80 + [char]0x93)) {
        # This is already handled in some cases, but en-dash is common
    }

    if ($modified) {
        [System.IO.File]::WriteAllText($file.FullName, $content, (New-Object System.Text.UTF8Encoding $false))
        Write-Host "  Fixed Mojibake in $($file.Name)"
    }
}
